class Map extends Component {
    static defaultProps = {
        center: {
            lat: 51.241379,
            lng: 22.566588
        },
        zoom: 13
    };

    render() {

        const { places } = this.props;

        return (

            <div style={{ height: '100%', width: '100%' }}>
                <GoogleMapReact
                    bootstrapURLKeys={
                        { key: 'AIzaSyCfCjUzcfjMplIWfRUT9c3JG89qTzxUwiw' }
                    }
                    defaultCenter={this.props.center}
                    defaultZoom={this.props.zoom}
                    options={{
                        styles,
                        disableDefaultUI: true
                    }}
                >
                    {places.map((place) => {
                        return (
                            <Place
                                key={place.uuid}
                                lat={place.latitude}
                                lng={place.longitude}
                                place={place}
                            />

                        )
                    })}

                </GoogleMapReact>
            </div>
        );
    }
}

const mapStateToProps = (state) => ({
    places: state.places.places
})

export default connect(mapStateToProps)(Map);